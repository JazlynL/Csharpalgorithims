public class IfLessThanZero {
    public class Program
    {
        public static bool LessThanOrEqualToZero(double a)
        {
            if(a <= 0){
                return true;
            }
            return false;
        }
    }


    /// remainder problem
    public class Program
    {
        public static int Remainder(int x, int y)
        {
            return x%y;
        }
    }
}
